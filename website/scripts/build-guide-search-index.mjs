/**
 * Build-time script: reads website/content/guide (.mdx),
 * extracts slug, title, description, and plain text (strips MDX/markdown).
 * Writes website/lib/guideSearchIndex.generated.ts for the client search.
 *
 * Run before dev/build: npm run build (prebuild) or npm run dev (runs this first).
 * New pages: add the slug to GUIDE_PAGES in lib/guideConfig.ts and add
 * content/guide/<slug>.mdx; then run build (or dev) to re-index.
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const CONTENT_DIR = path.join(ROOT, "content", "guide");
const OUT_FILE = path.join(ROOT, "lib", "guideSearchIndex.generated.ts");

// Source of truth: lib/guidePages.json (same as guideConfig.ts)
const GUIDE_ENTRIES = JSON.parse(
  fs.readFileSync(path.join(ROOT, "lib", "guidePages.json"), "utf-8")
);

function stripPlainText(raw) {
  if (!raw || typeof raw !== "string") return "";
  let text = raw
    .replace(/<[^>]+>/g, " ")
    .replace(/^#+\s*/gm, " ")
    .replace(/^>\s*/gm, " ")
    .replace(/\s+/g, " ")
    .trim();
  return text;
}

function parseFrontmatter(raw) {
  const match = raw.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n([\s\S]*)$/);
  if (!match) return { frontmatter: null, body: raw };
  const [, fm, body] = match;
  let title = "";
  let description = "";
  const titleM = fm.match(/title:\s*["']?([^"'\n]+)["']?/);
  const descM = fm.match(/description:\s*["']?([^"'\n]+)["']?/);
  if (titleM) title = titleM[1].trim();
  if (descM) description = descM[1].trim();
  return { title, description, body: body || "" };
}

function loadPage(slug, fallbackTitle) {
  const base = slug === "" ? "index" : slug;
  const mdxPath = path.join(CONTENT_DIR, `${base}.mdx`);
  let raw = null;
  try {
    if (fs.existsSync(mdxPath)) raw = fs.readFileSync(mdxPath, "utf-8");
  } catch {
    // missing or unreadable
  }
  let title = fallbackTitle;
  let description = "";
  let content = "";
  if (raw) {
    const { title: t, description: d, body } = parseFrontmatter(raw);
    if (t) title = t;
    if (d) description = d;
    content = stripPlainText(body);
  }
  return { slug, title, description, content };
}

const index = GUIDE_ENTRIES.map(({ slug, title }) => loadPage(slug, title));

const out = `// Generated by scripts/build-guide-search-index.mjs – do not edit by hand.
export interface GuideSearchEntry {
  slug: string;
  title: string;
  description: string;
  content: string;
}
export const guideSearchIndex: GuideSearchEntry[] = ${JSON.stringify(index, null, 2)};
`;

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, out, "utf-8");
console.log("[guide-search] Wrote", OUT_FILE, "with", index.length, "entries.");